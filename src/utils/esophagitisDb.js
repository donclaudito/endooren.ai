export const getAnatomicalReport = (id, xVal) => {
  const type = ANATOMICAL_TYPES.find(t => t.id === id);
  if (!type) return null;

  const text = type.manifestacaoEndoscopica.replace('[X]', xVal || '0');
  let conclusao = "";

  if (id === 'hernia_deslizamento') {
    conclusao = `Hérnia Hiatal por deslizamento (${xVal || '0'} cm)`;
  } else if (id === 'pinçamento_laxo') {
    conclusao = "Hiato diafragmático laxo (Sinal do Capuz)";
  }

  return {
    text,
    conclusao
  };
};

