export const getEsophagitisGrouped = () => {
  const groups = {};
  ESOPHAGITIS_TYPES.forEach(item => {
    if (!groups[item.categoria]) {
      groups[item.categoria] = [];
    }
    groups[item.categoria].push(item);
  });
  return groups;
};

export const getEsophagitisReport = (id, subclass, aiMode) => {
  const type = ESOPHAGITIS_TYPES.find(t => t.id === id);
  if (!type) return null;

  // Use the exact manifestacaoEndoscopica provided by the user
  let text = type.manifestacaoEndoscopica;

  // Append subclass score if defined
  if (subclass) {
    if (type.escoreSugerido === 'EREFS') {
      text += ` (Escore EREFS: ${subclass})`;
    } else if (type.escoreSugerido === 'Zargar') {
      text += ` (Classificação de Zargar: ${subclass})`;
    } else {
      text += ` (${subclass})`;
    }
  }

  // Base clinical conclusions and biopsy states
  let conclusao = type.nome;
  let biopsy = false;
  let biopsyDesc = "";

  // Customize clinical conclusions / biopsy requirements
  if (id.startsWith('peptica_la_')) {
    const letter = id.split('_').pop().toUpperCase();
    conclusao = `Esofagite de Refluxo (Los Angeles ${letter})`;
  } else if (id.startsWith('candida_kodsi_')) {
    const num = id.split('_').pop();
    const roman = num === '1' ? 'I' : num === '2' ? 'II' : num === '3' ? 'III' : 'IV';
    conclusao = `Candidíase Esofágica (Kodsi ${roman})`;
  } else if (id === 'imune_eosinofilica') {
    conclusao = "Suspeita de Esofagite Eosinofílica";
    biopsy = true;
    biopsyDesc = "Realizadas biópsias seriadas de esôfago (proximal e distal) para contagem de eosinófilos.";
  } else if (id === 'inf_hsv') {
    conclusao = "Suspeita de Esofagite por Herpes Simples (HSV)";
    biopsy = true;
    biopsyDesc = "Realizadas biópsias de lesões ulceradas em esôfago distal/médio.";
  } else if (id === 'inf_cmv') {
    conclusao = "Suspeita de Esofagite por Citomegalovírus (CMV)";
    biopsy = true;
    biopsyDesc = "Realizadas biópsias das bordas de lesões ulceradas em esôfago distal.";
  } else if (id === 'peptica_nerd') {
    conclusao = "Sinais sugestivos de Esofagite de Refluxo Não-Erosiva (NERD)";
  } else if (id === 'rara_estase') {
    conclusao = "Esofagite de Estase secundária a Megaesôfago / Acalásia";
  } else if (id === 'rara_isquemica') {
    conclusao = "Esofagite Isquêmica Necrosante (Esôfago Negro)";
  }

  return {
    text,
    conclusao,
    biopsy,
    biopsyDesc
  };
};

